#import "CZFriendButton.h"

@implementation CZFriendButton

-(CGRect)titleRectForContentRect:(CGRect)contentRect{
    CGFloat titleHeight = contentRect.size.height*0.25;
    CGFloat titleWidth  = contentRect.size.width;
    CGFloat x           = contentRect.size.height*0.0;
    CGFloat y           = contentRect.size.height*0.75;
    return CGRectMake(x, y, titleWidth, titleHeight);
}

-(CGRect)imageRectForContentRect:(CGRect)contentRect{
    CGFloat titleHeight = contentRect.size.height*0.75;
    CGFloat titleWidth  = contentRect.size.height*0.75;
    CGFloat x           = contentRect.size.height*0.125;
    CGFloat y           = contentRect.size.height*0;
    return CGRectMake(x, y, titleWidth, titleHeight);

}

@end
