#import "CZLeftImageButton.h"

@implementation CZLeftImageButton

-(CGRect)titleRectForContentRect:(CGRect)contentRect{
    CGFloat titleHeight = contentRect.size.height;
    CGFloat titleWidth  = contentRect.size.width - titleHeight;
    CGFloat x           = titleHeight + 8*2 ;
    CGFloat y           = 0;
    return CGRectMake(x, y, titleWidth, titleHeight);
}

-(CGRect)imageRectForContentRect:(CGRect)contentRect{
    CGFloat titleHeight = contentRect.size.height;
    CGFloat titleWidth  = contentRect.size.height;
    CGFloat x           = 8;
    CGFloat y           = 0;
    return CGRectMake(x, y, titleWidth, titleHeight);
    
}

@end
